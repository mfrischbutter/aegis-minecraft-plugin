package com.luascript.aegis.database.entity;

/**
 * Enum representing the action type for warning escalation.
 */
public enum ActionType {
    /**
     * Kick the player.
     */
    KICK,

    /**
     * Temporarily ban the player.
     */
    TEMPBAN,

    /**
     * Permanently ban the player.
     */
    PERMBAN
}
